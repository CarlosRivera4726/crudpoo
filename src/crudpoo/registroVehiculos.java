    private void btnRegistroVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroVehiculoActionPerformed
        // TODO add your handling code here:
        try {
            String usuario = CBUsuarios.getSelectedItem().toString();
            String placa = txtPlaca.getText();
            int numeroRuedas = Integer.parseInt(txtNumeroRuedas.getText());
            String color = txtColor.getText();
            String tipoVehiculo = CBVehiculos.getSelectedItem().toString();
            
            if(numeroRuedas >= 2 && numeroRuedas <= 4){
                con.registrarVehiculos(usuario, placa, numeroRuedas, color, tipoVehiculo);
            }
            
        } catch (NumberFormatException e) {
            System.out.println("Se ha ingresado un nÃºmero en el campo numeroRuedas!");
        }catch(SQLException ex){
            
        }


    }//GEN-LAST:event_btnRegistroVehiculoActionPerformed

